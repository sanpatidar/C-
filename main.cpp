#include<iostream>
using namespace std;
int main(){
    int num1,num2;
    cout<<"Enter the value of num1: ";/* << is called Insertion operation*/
    cin>>num1;/* >> is called Extraction operation*/

    cout<<"Enter the value of num2: ";/* << is called Insertion operation*/
    cin>>num2;/* >> is called Extraction operation*/

    cout<<"The sum is: "<<num1+num2;
    return 0;
}